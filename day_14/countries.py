def categorize_countries(pattern):
        def getctrs(lst):
            return list(filter(lambda x: x.lower().find(pattern)>=0, lst))
        return getctrs

stans = categorize_countries('stan')
iasc = categorize_countries('ia')
lands = categorize_countries('land')
islands = categorize_countries('island')

print(stans(countries))
print(iasc(countries))
print(lands(countries))
print(islands(countries))

def starting_letters_dict(letters, lst):
    countries_by_letter = dict()
    for letter in letters:
            countries_by_letter[letter.upper()] =  len(list(filter(lambda x: x.startswith(letter), lst))) 
    return countries_by_letter

ls_bl = starting_letters_dict(string.ascii_letters, countries)

print(ls_bl)

def get_first_ten_countries():
    def ten(lst):
        return reduce(lambda x, y: (x + ', ' + y), lst[:10]).split(', ')
    return ten

first_10 = get_first_ten_countries()

print(first_10(countries))

def get_last_ten_countries():
    def lten(lst):
        l = len(lst)
        return lst[l-10:]
    return lten        

last_10 = get_last_ten_countries()

print(last_10(countries))
